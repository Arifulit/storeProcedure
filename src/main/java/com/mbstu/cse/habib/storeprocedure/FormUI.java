    //Dynamically load combobox items from db
    private void loadComboBoxItem() {
        try{
            Connection connection = DBConnect.getConnection();
            String sql = "select distinct dept from student;";
            PreparedStatement statement;
            statement = connection.prepareStatement(sql);
            ResultSet resultSet;
            resultSet = statement.executeQuery();
            while (resultSet.next()) {
                String deptName = resultSet.getString("dept");
                DepartmentComboBox.addItem(deptName);
            }
            resultSet.close();
            statement.close();
        }
        catch(SQLException ex){
            Logger.getLogger(StoreProcedure.class.getName()).log(Level.SEVERE, null, ex);
        }
//        DBConnect.closeConnection();
    }
    
    // Method to convert ResultSet to DefaultTableModel
    public static DefaultTableModel buildTableModel(ResultSet rs) throws SQLException {
        ResultSetMetaData metaData = rs.getMetaData();

        // Get column names
        int columnCount = metaData.getColumnCount();
        String[] columnNames = new String[columnCount];
        for (int column = 1; column <= columnCount; column++) {
            columnNames[column - 1] = metaData.getColumnName(column);
        }

        // Get data rows
        DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0);
        while (rs.next()) {
            Object[] rowData = new Object[columnCount];
            for (int i = 0; i < columnCount; i++) {
                rowData[i] = rs.getObject(i + 1);
            }
            tableModel.addRow(rowData);
        }

        return tableModel;
    }

