    private void ViewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewButtonActionPerformed
        try{
            // Get parameter values from UI elements
        String id = IDField.getText().trim(); // trim leading/trailing spaces
        String name = NameField.getText().trim();
        String dept = (String) DepartmentComboBox.getSelectedItem(); // cast to String
        String balanceFromStr = jTextField4.getText().trim();
        String balanceToStr = jTextField2.getText().trim();
        
        // Convert balance strings to integers (check for parsing errors)
//        int balanceFrom = Integer.parseInt(balanceFromStr);
//        int balanceTo = Integer.parseInt(balanceToStr);
        
            
            Connection connection = DBConnect.getConnection();
            String sql = "CALL sp_selection_info(?, ?, ?, ?, ?)";
            PreparedStatement statement;
            
            
            statement = connection.prepareStatement(sql);
        statement.setString(1, id.isEmpty() ? null : id); // set NULL if empty
        statement.setString(2, name.isEmpty() ? null : name); // set NULL if empty
        statement.setString(3, "All".equals(dept) ? null : dept); // set NULL if empty
        statement.setInt(4, balanceFromStr.isEmpty()? 0 : Integer.parseInt(balanceFromStr));
        statement.setInt(5, balanceToStr.isEmpty()? 999999 : Integer.parseInt(balanceToStr));
        
            ResultSet resultSet;
            resultSet = statement.executeQuery();
            
            JTable table = new JTable(buildTableModel(resultSet));
            
            // Create dialog box to show the table
            JDialog dialog = new JDialog(this, "Database Table");
            dialog.add(new JScrollPane(table));
            dialog.setSize(400, 300);
            dialog.setVisible(true);
                
            resultSet.close();
            statement.close();
        }
        catch(SQLException ex){
            Logger.getLogger(StoreProcedure.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ViewButtonActionPerformed

